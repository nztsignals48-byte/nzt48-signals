/// Apply time-based confidence decay to entry signal.
/// Confidence degrades at 2.1% per hour held.
/// Entry loses edge as opportunity window closes.
pub fn apply_confidence_decay(base_confidence: f64, hours_held: f64) -> f64 {
    let decay_rate = 2.1;  // % per hour
    let decayed = base_confidence - (decay_rate * hours_held);
    decayed.max(0.0)  // Clamp at 0 (no negative confidence)
}

#[cfg(test)]
mod decay_tests {
    use super::*;

    #[test]
    fn test_confidence_no_decay_fresh() {
        assert_eq!(apply_confidence_decay(82.0, 0.0), 82.0);
    }

    #[test]
    fn test_confidence_decays_one_hour() {
        let result = apply_confidence_decay(82.0, 1.0);
        assert!((result - 79.9).abs() < 0.01);
    }

    #[test]
    fn test_confidence_decays_multiple_hours() {
        let result = apply_confidence_decay(82.0, 5.0);
        assert!((result - 71.5).abs() < 0.01);
    }

    #[test]
    fn test_confidence_clamped_at_zero() {
        let result = apply_confidence_decay(82.0, 40.0);
        assert_eq!(result, 0.0);
    }

    #[test]
    fn test_confidence_decay_various_bases() {
        assert!((apply_confidence_decay(65.0, 10.0) - 44.0).abs() < 0.01);
        assert!((apply_confidence_decay(72.0, 15.0) - 40.5).abs() < 0.01);
    }
}
