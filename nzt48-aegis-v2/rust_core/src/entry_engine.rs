/// Type C: Short overbought via inverse ETPs when price-volume divergence appears.
/// Catches reversal when strong price moves but volume declining.
pub struct OverboughtFadeDetector;

impl OverboughtFadeDetector {
    /// Detect Type C entry: Overbought + divergence (price up, volume down)
    /// Triggers when:
    /// - RSI > 75 (overbought, ready to pull back)
    /// - Volume divergence confirmed (price rising but volume declining)
    /// Base confidence: 72%
    ///
    /// Note: volume_divergence_confirmed should be pre-calculated
    /// (price_pct_change > 0 AND volume < vol_ma20)
    pub fn detect(
        rsi: f64,
        vol_divergence_confirmed: bool,
        ticker_id: TickerId,
    ) -> Option<EntrySignal> {
        let trigger = rsi > 75.0 && vol_divergence_confirmed;

        if trigger {
            Some(EntrySignal {
                entry_type: EntryType::OverboughtFade,
                base_confidence: 72.0,
                ticker_id,
                side: Direction::Long,  // Actual trade would be SHORT via inverse ETP
                strategy: StrategyId::ApexScout,
            })
        } else {
            None
        }
    }
}

