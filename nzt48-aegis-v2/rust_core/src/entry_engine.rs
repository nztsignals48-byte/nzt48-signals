/// Type D: Daily low bounce when price bounces back to support level.
/// Safest entry: buying right at support with RSI confirmation.
pub struct SupportBounceDetector;

impl SupportBounceDetector {
    /// Detect Type D entry: Price near daily low + RSI oversold
    /// Triggers when:
    /// - Current price within 1% of daily low
    /// - RSI 20-40 (panic oversold, recovery possible)
    ///   Base confidence: 70%
    pub fn detect_cfg(
        current_price: f64,
        daily_low: f64,
        rsi: f64,
        ticker_id: TickerId,
        cfg: &EntryTypeConfig,
    ) -> Option<EntrySignal> {
        if daily_low <= 0.0 {
            return None;
        }

        let price_pct_above_low = ((current_price - daily_low) / daily_low) * 100.0;
        let trigger = price_pct_above_low <= cfg.type_d_price_proximity_pct
            && rsi >= cfg.type_d_rsi_low && rsi <= cfg.type_d_rsi_high;

        if trigger {
            Some(EntrySignal {
                entry_type: EntryType::SupportBounce,
                base_confidence: cfg.type_d_confidence,
                ticker_id,
                side: Direction::Long,
                strategy: StrategyId::VanguardSniper,
            })
        } else {
            None
        }
    }

    /// Legacy detect with hardcoded defaults.
    pub fn detect(
        current_price: f64,
        daily_low: f64,
        rsi: f64,
        ticker_id: TickerId,
    ) -> Option<EntrySignal> {
        Self::detect_cfg(current_price, daily_low, rsi, ticker_id, &EntryTypeConfig::default())
    }
}

