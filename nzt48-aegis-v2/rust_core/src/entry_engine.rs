/// Entry signal classification
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum EntryType {
    /// Type A: Support bounce from recent lows (65% base confidence)
    DipRecovery,
    /// Type B: Momentum before overbought forms — YOUR EDGE (82% base confidence)
    EarlyRunner,
    /// Type C: Shorting via inverse ETPs (72% base confidence)
    OverboughtFade,
    /// Type D: Daily low bounce (70% base confidence)
    SupportBounce,
    /// Type E: IBS mean reversion (IBS < 0.1 + RVOL > 1.0 + mean_reverting)
    IBSMeanReversion,
    /// Type F: OBV divergence (OBV-RSI < 30 + RVOL > 0.7)
    OBVDivergence,
}

/// Entry signal generated by any entry detector
#[derive(Debug, Clone)]
pub struct EntrySignal {
    /// Which entry type triggered
    pub entry_type: EntryType,
    /// Base confidence before decay [0.0, 100.0]
    pub base_confidence: f64,
    /// Interned ticker ID for this entry
    pub ticker_id: TickerId,
    /// Direction (Long only in ISA)
    pub side: Direction,
    /// Strategy that generated this
    pub strategy: StrategyId,
}

impl EntrySignal {
    /// Create a new entry signal with all required fields
    pub fn new(
        entry_type: EntryType,
        base_confidence: f64,
        ticker_id: TickerId,
        side: Direction,
        strategy: StrategyId,
    ) -> Self {
        Self {
            entry_type,
            base_confidence: base_confidence.clamp(0.0, 100.0),
            ticker_id,
            side,
            strategy,
        }
    }
}

