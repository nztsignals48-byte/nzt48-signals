/// Type D: Daily low bounce when price bounces back to support level.
/// Safest entry: buying right at support with RSI confirmation.
pub struct SupportBounceDetector;

impl SupportBounceDetector {
    /// Detect Type D entry: Price near daily low + RSI oversold
    /// Triggers when:
    /// - Current price within 1% of daily low
    /// - RSI 20-40 (panic oversold, recovery possible)
    /// Base confidence: 70%
    pub fn detect(
        current_price: f64,
        daily_low: f64,
        rsi: f64,
        ticker_id: TickerId,
    ) -> Option<EntrySignal> {
        // Calculate how far above daily low
        if daily_low <= 0.0 {
            return None; // Invalid daily low
        }

        let price_pct_above_low = ((current_price - daily_low) / daily_low) * 100.0;
        let trigger = price_pct_above_low <= 1.0 // Within 1% of daily low
            && rsi >= 20.0 && rsi <= 40.0;       // Oversold but recovering

        if trigger {
            Some(EntrySignal {
                entry_type: EntryType::SupportBounce,
                base_confidence: 70.0,
                ticker_id,
                side: Direction::Long,
                strategy: StrategyId::VanguardSniper,
            })
        } else {
            None
        }
    }
}

