/// Type B: Momentum building before overbought forms — YOUR EDGE.
/// Catches the uptrend before RSI hits 70.
pub struct EarlyRunnerDetector {
    rvol_history: VecDeque<f64>,
}

impl EarlyRunnerDetector {
    /// Create detector with empty history
    pub fn new() -> Self {
        Self {
            rvol_history: VecDeque::with_capacity(5),
        }
    }

    /// Detect Type B entry with configurable thresholds.
    pub fn detect_cfg(
        &mut self,
        current_rvol: f64,
        rsi: f64,
        ticker_id: TickerId,
        cfg: &EntryTypeConfig,
    ) -> Option<EntrySignal> {
        self.rvol_history.push_back(current_rvol);
        if self.rvol_history.len() > cfg.type_b_momentum_bars {
            self.rvol_history.pop_front();
        }

        if self.rvol_history.len() < cfg.type_b_momentum_bars {
            return None;
        }

        let vals: Vec<f64> = self.rvol_history.iter().copied().collect();
        let rising = vals.windows(2).all(|w| w[0] < w[1]);

        let trigger = rising
            && rsi > cfg.type_b_rsi_low && rsi < cfg.type_b_rsi_high;

        if trigger {
            Some(EntrySignal {
                entry_type: EntryType::EarlyRunner,
                base_confidence: cfg.type_b_confidence,
                ticker_id,
                side: Direction::Long,
                strategy: StrategyId::VanguardSniper,
            })
        } else {
            None
        }
    }

    /// Legacy detect with hardcoded defaults.
    pub fn detect(
        &mut self,
        current_rvol: f64,
        rsi: f64,
        ticker_id: TickerId,
    ) -> Option<EntrySignal> {
        self.detect_cfg(current_rvol, rsi, ticker_id, &EntryTypeConfig::default())
    }

    /// Reset history (useful for testing or ticker switch)
    pub fn reset(&mut self) {
        self.rvol_history.clear();
    }
}

impl Default for EarlyRunnerDetector {
    fn default() -> Self {
        Self::new()
    }
}

