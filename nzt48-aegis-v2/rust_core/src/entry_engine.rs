/// Trading tier classification
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Tier {
    /// Tier One: Conservative, support bounces only
    One,
    /// Tier Two: Moderate, all 4 entry types allowed
    Two,
    /// Tier Three: Aggressive, momentum + fade only
    Three,
    /// Tier Four: Dormant, no trading
    Four,
}

/// Select which entry types are available for a given tier
pub fn select_entry_types_by_tier(tier: Tier) -> &'static [EntryType] {
    match tier {
        Tier::One => &[EntryType::SupportBounce],  // Safest
        Tier::Two => &[
            EntryType::DipRecovery,
            EntryType::EarlyRunner,
            EntryType::OverboughtFade,
            EntryType::SupportBounce,
        ],
        Tier::Three => &[EntryType::EarlyRunner, EntryType::OverboughtFade],  // Aggressive
        Tier::Four => &[],  // No trading
    }
}

