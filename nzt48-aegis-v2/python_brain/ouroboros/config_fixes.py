# v19-P2-3: Polygon→IBKR Reverse Symbology Mapping
# ---------------------------------------------------------------------------
# Polygon uses tickers like "LSE:NVD3" or "XLON:QQQ3".
# IBKR uses "NVD3.L" (symbol + ".L" suffix for LSE).

POLYGON_TO_IBKR_EXCHANGE_MAP = {
    "LSE": ".L",
    "XLON": ".L",
    "XETRA": ".DE",
    "FRA": ".F",
    "EURONEXT": ".AS",  # Amsterdam default; Paris = .PA
    "EPA": ".PA",
    "TSE": ".T",
    "HKEX": ".HK",
    "SGX": ".SI",
    "KSE": ".KS",
    "ASX": ".AX",
}

# US exchanges don't need suffix
US_EXCHANGES = {"NYSE", "NASDAQ", "AMEX", "BATS", "ARCA", "IEX"}


def polygon_to_ibkr(polygon_ticker: str) -> str:
    """Convert Polygon ticker to IBKR format.

    Examples:
        "LSE:NVD3" → "NVD3.L"
        "XLON:QQQ3" → "QQQ3.L"
        "AAPL" → "AAPL"
        "TSE:7203" → "7203.T"
    """
    if ":" in polygon_ticker:
        exchange, symbol = polygon_ticker.split(":", 1)
        exchange = exchange.upper()

        if exchange in US_EXCHANGES:
            return symbol

        suffix = POLYGON_TO_IBKR_EXCHANGE_MAP.get(exchange, "")
        if suffix:
            return f"{symbol}{suffix}"

        log.warning(f"Unknown Polygon exchange: {exchange} for ticker {polygon_ticker}")
        return symbol

    return polygon_ticker  # Already in IBKR format or US ticker


def ibkr_to_polygon(ibkr_ticker: str) -> str:
    """Convert IBKR ticker to Polygon format.

    Examples:
        "NVD3.L" → "LSE:NVD3"
        "AAPL" → "AAPL"
        "7203.T" → "TSE:7203"
    """
    IBKR_TO_POLYGON = {v: k for k, v in POLYGON_TO_IBKR_EXCHANGE_MAP.items()}

    for suffix, exchange in sorted(IBKR_TO_POLYGON.items(), key=lambda x: -len(x[0])):
        if ibkr_ticker.endswith(suffix):
            symbol = ibkr_ticker[:-len(suffix)]
            return f"{exchange}:{symbol}"

    return ibkr_ticker  # US ticker


