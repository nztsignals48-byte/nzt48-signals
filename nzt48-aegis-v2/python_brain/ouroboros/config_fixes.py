def main():
    import argparse
    parser = argparse.ArgumentParser(description="Config fixes and infrastructure utilities")
    parser.add_argument("--write-ftt", action="store_true", help="Write FTT registry TOML")
    parser.add_argument("--test-polygon", type=str, help="Convert Polygon ticker to IBKR")
    parser.add_argument("--test-ibkr", type=str, help="Convert IBKR ticker to Polygon")
    parser.add_argument("--asx-open", action="store_true", help="Show current ASX open time")
    parser.add_argument("--shadow-threshold", type=float, help="Compute shadow book threshold for position value")
    parser.add_argument("--all", action="store_true", help="Run all checks")
    args = parser.parse_args()

    if args.write_ftt or args.all:
        path = write_ftt_config()
        print(f"FTT registry written to {path}")
        for country, tax in sorted(TRANSACTION_TAX_REGISTRY.items()):
            exempt = "YES" if tax["intraday_exempt"] else "NO"
            print(f"  {country}: {tax['rate_bps']}bps, intraday_exempt={exempt}")

    if args.test_polygon:
        result = polygon_to_ibkr(args.test_polygon)
        print(f"Polygon '{args.test_polygon}' → IBKR '{result}'")

    if args.test_ibkr:
        result = ibkr_to_polygon(args.test_ibkr)
        print(f"IBKR '{args.test_ibkr}' → Polygon '{result}'")

    if args.asx_open or args.all:
        hour, minute = get_asx_open_utc()
        dst = is_australian_dst()
        print(f"\nASX Open: {hour:02d}:{minute:02d} UTC (DST={'AEDT' if dst else 'AEST'})")

    if args.shadow_threshold is not None:
        threshold = shadow_book_threshold(args.shadow_threshold)
        print(f"Shadow book threshold for £{args.shadow_threshold:.0f} position: £{threshold:.2f}")

    if args.all:
        # Test some Polygon conversions
        test_tickers = ["LSE:NVD3", "XLON:QQQ3", "AAPL", "TSE:7203", "EPA:TTE"]
        print("\nPolygon→IBKR mapping test:")
        for t in test_tickers:
            print(f"  {t} → {polygon_to_ibkr(t)}")

        print(f"\nShadow book: threshold(£1000) = £{shadow_book_threshold(1000):.2f}")
        print(f"Shadow book: threshold(£100) = £{shadow_book_threshold(100):.2f}")

        print(f"\nFTT cost: FR intraday = {ftt_cost_bps('FR', True)}bps (exempt)")
        print(f"FTT cost: FR overnight = {ftt_cost_bps('FR', False)}bps")
        print(f"FTT cost: UK intraday = {ftt_cost_bps('UK', True)}bps (NOT exempt)")


if __name__ == "__main__":
    main()
