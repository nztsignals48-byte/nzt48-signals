# v19-P2-1: Shadow Book Alert Threshold
# ---------------------------------------------------------------------------
# Current threshold £5 causes excessive Telegram alerts.
# Raise to max(£50, 0.5% of position value).

SHADOW_BOOK_THRESHOLD_GBP = 50.0
SHADOW_BOOK_THRESHOLD_PCT = 0.005  # 0.5%


def shadow_book_threshold(position_value_gbp: float) -> float:
    """Compute shadow book alert threshold.

    Returns max(£50, 0.5% of position value).
    Telegram alert fires only if shadow book divergence exceeds this.
    """
    return max(SHADOW_BOOK_THRESHOLD_GBP, position_value_gbp * SHADOW_BOOK_THRESHOLD_PCT)


