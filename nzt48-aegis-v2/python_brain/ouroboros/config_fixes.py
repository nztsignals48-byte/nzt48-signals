# P2-7: FTT (Financial Transaction Tax) Intraday Exemption
# ---------------------------------------------------------------------------
# French FTT: 0.30% on settled (T+2) purchases of French equities > €1B market cap
# Italian FTT: 0.10% on settled purchases of Italian equities
# Both EXEMPT intraday round-trips (entry + exit same calendar day).

# Store as integer basis points to avoid floating-point precision issues (v19-P2-7)
TRANSACTION_TAX_REGISTRY = {
    "FR": {
        "name": "France Financial Transaction Tax",
        "rate_bps": 30,          # 0.30% = 30 basis points
        "applies_to": "buy",     # Only on purchases
        "min_market_cap_eur": 1_000_000_000,  # €1B threshold
        "intraday_exempt": True,  # Same-day round-trips exempt
        "settlement": "T+2",
    },
    "IT": {
        "name": "Italy Financial Transaction Tax",
        "rate_bps": 10,          # 0.10% = 10 basis points
        "applies_to": "buy",
        "min_market_cap_eur": 500_000_000,
        "intraday_exempt": True,
        "settlement": "T+2",
    },
    "UK": {
        "name": "UK Stamp Duty Reserve Tax",
        "rate_bps": 50,          # 0.50% = 50 basis points
        "applies_to": "buy",
        "min_market_cap_eur": 0,  # All UK equities
        "intraday_exempt": False,  # UK SDRT applies regardless
        "settlement": "T+2",
        "note": "ETPs listed on LSE are generally EXEMPT from SDRT",
    },
    "IE": {
        "name": "Ireland Stamp Duty",
        "rate_bps": 100,         # 1.00%
        "applies_to": "buy",
        "min_market_cap_eur": 0,
        "intraday_exempt": False,
        "settlement": "T+2",
    },
}


def generate_ftt_toml() -> str:
    """Generate transaction_tax_registry.toml content.

    Uses integer basis points (not float percentages) per v19-P2-7.
    """
    lines = [
        "# Transaction Tax Registry — auto-generated by config_fixes.py",
        "# Rates stored as INTEGER BASIS POINTS to avoid float precision issues (v19-P2-7)",
        "# 30 bps = 0.30%, 10 bps = 0.10%, 50 bps = 0.50%",
        "",
    ]

    for country, tax in sorted(TRANSACTION_TAX_REGISTRY.items()):
        lines.append(f'[{country}]')
        lines.append(f'name = "{tax["name"]}"')
        lines.append(f'rate_bps = {tax["rate_bps"]}')
        lines.append(f'applies_to = "{tax["applies_to"]}"')
        lines.append(f'min_market_cap_eur = {tax["min_market_cap_eur"]}')
        lines.append(f'intraday_exempt = {"true" if tax["intraday_exempt"] else "false"}')
        lines.append(f'settlement = "{tax["settlement"]}"')
        if "note" in tax:
            lines.append(f'note = "{tax["note"]}"')
        lines.append("")

    return "\n".join(lines)


def write_ftt_config(config_dir: Path = CONFIG_DIR) -> Path:
    """Write transaction_tax_registry.toml atomically."""
    path = config_dir / "transaction_tax_registry.toml"
    content = generate_ftt_toml()

    tmp_path = path.with_suffix(".toml.tmp")
    tmp_path.write_text(content)
    os.rename(str(tmp_path), str(path))

    log.info(f"P2-7: Wrote FTT registry: {path} ({len(TRANSACTION_TAX_REGISTRY)} jurisdictions)")
    return path


def is_ftt_exempt(country: str, is_intraday: bool) -> bool:
    """Check if a trade is FTT-exempt.

    Args:
        country: ISO 2-letter country code
        is_intraday: True if entry and exit on same calendar day

    Returns:
        True if exempt from FTT
    """
    tax = TRANSACTION_TAX_REGISTRY.get(country)
    if tax is None:
        return True  # No FTT for this country
    if is_intraday and tax.get("intraday_exempt", False):
        return True
    return False


def ftt_cost_bps(country: str, is_intraday: bool) -> int:
    """Get FTT cost in basis points (0 if exempt)."""
    if is_ftt_exempt(country, is_intraday):
        return 0
    tax = TRANSACTION_TAX_REGISTRY.get(country, {})
    return tax.get("rate_bps", 0)


