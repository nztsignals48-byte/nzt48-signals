# v19-P2-9: ASX DST Detection (AEDT vs AEST)
# ---------------------------------------------------------------------------
# ASX session open differs by 1 hour depending on Australian DST:
#   AEDT (UTC+11): ASX opens at 23:00 UTC (previous day)
#   AEST (UTC+10): ASX opens at 00:00 UTC

def get_asx_open_utc(when: Optional[datetime] = None) -> Tuple[int, int]:
    """Get ASX opening time in UTC, accounting for Australian DST.

    Returns:
        (hour, minute) in UTC when ASX opens
    """
    if when is None:
        when = datetime.now(timezone.utc)

    sydney_tz = ZoneInfo("Australia/Sydney")
    sydney_now = when.astimezone(sydney_tz)
    utc_offset = sydney_now.utcoffset()

    if utc_offset is None:
        # Fallback: assume AEST (UTC+10)
        return (0, 0)

    offset_hours = utc_offset.total_seconds() / 3600

    # ASX opens at 10:00 local Sydney time
    asx_local_open_hour = 10
    asx_open_utc_hour = int(asx_local_open_hour - offset_hours) % 24

    if offset_hours == 11:  # AEDT
        log.info("v19-P2-9: ASX DST=AEDT (UTC+11), open=23:00 UTC (prev day)")
    else:  # AEST
        log.info(f"v19-P2-9: ASX DST=AEST (UTC+{offset_hours:.0f}), open={asx_open_utc_hour:02d}:00 UTC")

    return (asx_open_utc_hour, 0)


def is_australian_dst(when: Optional[datetime] = None) -> bool:
    """Check if Australia is currently in daylight saving time."""
    if when is None:
        when = datetime.now(timezone.utc)

    sydney_tz = ZoneInfo("Australia/Sydney")
    sydney_now = when.astimezone(sydney_tz)
    offset = sydney_now.utcoffset()

    if offset is None:
        return False

    return offset.total_seconds() / 3600 == 11


