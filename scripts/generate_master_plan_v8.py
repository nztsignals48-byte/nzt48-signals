def _publish_pdf(path: str) -> None:
    """Copy finished PDF to Desktop/NZT-48 CORE PDFS, deleting old version."""
    desktop_dir = os.path.join(os.path.expanduser("~"), "Desktop", "NZT-48 CORE PDFS")
    os.makedirs(desktop_dir, exist_ok=True)
    dest = os.path.join(desktop_dir, os.path.basename(path))
    if os.path.exists(dest):
        os.remove(dest)
        print(f"‚úď Deleted old: {dest}")
    shutil.copy2(path, dest)
    print(f"‚úď PDF written: {path}")
    print(f"‚úď Copied to Desktop: {dest}")


