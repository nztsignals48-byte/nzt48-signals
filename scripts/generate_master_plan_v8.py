def make_doc(path):
    doc = BaseDocTemplate(path, pagesize=A4,
                          leftMargin=LMAR, rightMargin=RMAR,
                          topMargin=14*mm, bottomMargin=14*mm)
    frame = Frame(LMAR, 14*mm, CONTENT_W, H - 28*mm, id="main")

    def _header_footer(canvas, doc):
        canvas.saveState()
        # Top stripe
        canvas.setFillColor(D2)
        canvas.rect(0, H - 10*mm, W, 10*mm, fill=1, stroke=0)
        canvas.setFont("Helvetica-Bold", 7)
        canvas.setFillColor(GOLD)
        canvas.drawString(LMAR, H - 6*mm, "NZT-48  ¬∑  Master Plan v8.0  ¬∑  STRICTLY CONFIDENTIAL")
        canvas.setFont("Helvetica", 7)
        canvas.setFillColor(GREY)
        canvas.drawRightString(W - RMAR, H - 6*mm, f"Page {doc.page}  ¬∑  {datetime.now(timezone.utc).strftime('%d %b %Y')}")
        # Bottom stripe
        canvas.setFillColor(D2)
        canvas.rect(0, 0, W, 10*mm, fill=1, stroke=0)
        canvas.setFont("Helvetica-Oblique", 6.5)
        canvas.setFillColor(GREY)
        canvas.drawString(LMAR, 3.5*mm,
            "Confidential ‚ÄĒ Internal Use Only ¬∑ Academic citations are analytical benchmarks")
        canvas.restoreState()

    doc.addPageTemplates([PageTemplate(id="main", frames=[frame],
                                       onPage=_header_footer)])
    return doc

