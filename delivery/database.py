def get_consecutive_losses(conn: sqlite3.Connection, bot_instance: str) -> int:
    """Count consecutive losses for emotional firewall (paper + live).

    B-06 (SK-02): Uses ScopedQuery to scope to current trading session
    (06:00 UK today). Old code used datetime('now', '-12 hours') which
    suffered from BST drift and could still match stale losses from the
    previous session, causing permanent deadlock ('zombie halt').
    """
    sq = ScopedQuery(conn)
    # Prefer virtual_trades (paper mode), fall back to trades (live mode)
    rows = sq.execute(
        """SELECT net_pnl as pnl FROM virtual_trades
           WHERE bot_instance = ?
           ORDER BY exit_time DESC LIMIT 10""",
        (bot_instance,),
        time_column="exit_time",
    ).fetchall()
    if not rows:
        rows = sq.execute(
            """SELECT pnl_dollars as pnl FROM trades
               WHERE bot_instance = ?
               ORDER BY time_exited DESC LIMIT 10""",
            (bot_instance,),
            time_column="time_exited",
        ).fetchall()
    count = 0
    for row in rows:
        if (row["pnl"] or 0) < 0:
            count += 1
        else:
            break
    return count


