def get_daily_pnl(conn: sqlite3.Connection,
                   bot_instance: str | None = None,
                   date: str | None = None) -> float:
    """Get combined P&L for today across specified bot or all bots (paper + live)."""
    if date is None:
        date_clause_trades = "date(time_entered) = date('now')"
        date_clause_vtrades = "date(exit_time) = date('now')"
        params: list[Any] = []
    else:
        date_clause_trades = "date(time_entered) = ?"
        date_clause_vtrades = "date(exit_time) = ?"
        params = [date, date]  # one for each subquery

    if bot_instance:
        query = f"""SELECT COALESCE(SUM(pnl), 0) FROM (
            SELECT pnl_dollars as pnl FROM trades WHERE bot_instance = ? AND {date_clause_trades}
            UNION ALL
            SELECT net_pnl as pnl FROM virtual_trades WHERE bot_instance = ? AND {date_clause_vtrades}
        )"""
        params = [bot_instance] + ([] if date is None else [date]) + [bot_instance] + ([] if date is None else [date])
    else:
        query = f"""SELECT COALESCE(SUM(pnl), 0) FROM (
            SELECT pnl_dollars as pnl FROM trades WHERE {date_clause_trades}
            UNION ALL
            SELECT net_pnl as pnl FROM virtual_trades WHERE {date_clause_vtrades}
        )"""

    return conn.execute(query, params).fetchone()[0]


