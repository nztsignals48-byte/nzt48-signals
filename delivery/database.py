def _validate_columns(table: str, columns: set[str] | list[str]) -> None:
    """Validate column names against the whitelist. Raises ValueError on invalid columns."""
    valid = VALID_COLUMNS.get(table)
    if valid is None:
        raise ValueError(f"Unknown table: {table}")
    invalid = set(columns) - valid
    if invalid:
        raise ValueError(f"Invalid column names for table '{table}': {invalid}")


def get_connection(db_path: str | Path | None = None) -> sqlite3.Connection:
    """Create a new SQLite connection with WAL mode for concurrent reads."""
    if db_path is None:
        db_path = DEFAULT_DB_PATH
    db_path = Path(db_path)
    db_path.parent.mkdir(parents=True, exist_ok=True)

    conn = sqlite3.connect(str(db_path), timeout=30)
    conn.execute("PRAGMA journal_mode=WAL")
    conn.execute("PRAGMA foreign_keys=ON")
    conn.execute("PRAGMA busy_timeout=5000")
    conn.row_factory = sqlite3.Row
    return conn


@contextmanager
def transaction(db_path: str | Path | None = None) -> Generator[sqlite3.Connection, None, None]:
    """Context manager for database transactions with auto-commit/rollback."""
    conn = get_connection(db_path)
    try:
        yield conn
        conn.commit()
    except Exception:
        conn.rollback()
        raise
    finally:
        conn.close()


